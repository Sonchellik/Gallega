<h2>Política de Cookies</h2>
<h4>¿Que son las Cookies?</h4>

<p>Una cookie es un fichero que se descarga en tu ordenador al acceder a determinadas páginas web. Las cookies permiten a una página web, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un usuario o de su equipo, gestionar el acceso de usuarios a zonas restringidas de la web …etc.</p>

<h4>¿Que tipo de Cookies utiliza esta página web?</h4>

<p>COOKIES DE ANÁLISIS<p>

<p>Son aquéllas que posibilitan el seguimiento y análisis del comportamiento de los usuarios en nuestra página. La información recogida se utiliza para la medición de la actividad de los usuarios en la web y la elaboración de perfiles de navegación de los usuarios, con la finalidad de mejorar la web, así como los productos y servicios ofertados. </p>

<p>COOKIES TÉCNICAS</p>
<p>
Son aquéllas que facilitan al usuario la navegación a través de la web y la utilización de las diferentes opciones o servicios que existen en la página como por ejemplo la identificación de sesión, acceso a zonas restringidas, recuperación de los elementos introducidos en el sistema de pedidos …etc.</p>

<p>COOKIES DE PERSONALIZACIÓN</p>

<p>Son aquéllas que permiten al usuario acceder a la web con una serie de caracteríticas técnicas predeterminadas, según los criterios preestablecidos en su equipo, como por ejemplo el idioma, el tipo de navegador, la configuración regional….etc.
</p>

<h4>¿Quien utiliza las Cookies?</h4>

<p>Las cookies empleadas en esta página web, son utilizadas por el responsable legal de la misma. No obstante, las cookies de análisis son gestionadas a través de Google Analytics, un servicio de analítica web prestado por Google, Inc., cuya oficina principal está situada en 1600 Amphitheatre Parkway, Mountain View (California), CA 94043, Estados Unidos (“Google”). Google Analytics utiliza sus cookies para analizar la navegación de los usuarios en este sitio web. La información que genera la cookie acerca del uso de la página (incluyendo su dirección IP) será directamente transmitida y archivada por Google en los servidores de Estados Unidos. Google usará esta información para proporcionarnos información sobre la actividad de los usuarios en nuestra webt. Google podrá transmitir dicha información a terceros cuando así se lo requiera la legislación, o cuando dichos terceros procesen la información por cuenta de Google. Google no asociará su dirección IP con ningún otro dato del que disponga Google. Si desea saber mas sobre los términos de privacidad de este servicio puede informarse en la siguiente página web. <a href="http://www.google.com/intl/es/analytics/privacyoverview.html" target="_new">Google</a></p>

